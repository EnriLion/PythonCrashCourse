#Using the range() Function
for Val in range(1, 5):
    print(Val)

#In this example range() prints only the numbers 1 through 4 the range() function causes Python to start counting at the first Val to give it, and it stops when it reaches the second Val you provide.

#Print  the numbers from 1 to 5
print("\n")

for Val in range(1, 6):
    print(Val)


#Using range() to Make a list of Numbers
print("\n")

Num = list(range(1, 6))
