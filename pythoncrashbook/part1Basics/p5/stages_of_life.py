person = 15
if person < 2:
    print(f"You are a baby")
elif person >= 2 and person < 4:
    print("You are a toddler")
elif person >= 4 and person < 13:
    print(f"You are a kid")
elif person >= 13 and person < 20:
    print(f"You are a teenager")
elif person >= 20 and person < 65:
    print(f"You are an adult")
else:
    print(f"You are an elder")




